# 문자열 초기화
# 문자열은 기본적으로 '', ""를 이용
data = 'Hello World'
print(data)  # Hello World

data = "Don't you know \"Python\"?"
print(data)  # Don't you know "Python"?

# 문자열 연산
a = "Hello"
b = "World"

print(a + " " + b)  # Hello World

a = "String"
print(a*3)  # StringStringString

a = "ABCDEF"
print(a[2:4])  # CD
